     */
    public function index(Request $request)
    {
        if($request->unit_id) {
            $questions = Questions::where('unit_id', $request->unit_id)->with('answer')->get()->toArray();
        } else {
            $questions = Questions::get()->toArray();
        }
        return response()->json([
            'questions' => $questions
        ]);

    }

